public class Java_Data_Types {
  public static void main(String[] args) {
    System.out.println("Hello world!");
    // Declare variables with different data types 
    int age = 25; // Integer (whole numbers) 
    double pi = 3.14159; // Double (floating-point numbers) 
    char initial = 'A'; // Character (single character) 
    boolean isStudent = true; // Boolean (true or false) 
    String name = "John Doe"; // String (sequence of characters)
    System.out.println("Age: " + age);
    System.out.println("Pi: " + pi);
    System.out.println("Initial: " + initial);
    System.out.println("Is student: " + isStudent);
    System.out.println("Name: " + name);
  }
}
